def unet_reg_v1(input, is_training, style, num_output_channels):

    model = seg_unet_reg_v1(unet_size=style, num_classes=num_output_channels, data_format='channels_last')
    output = model(inputs=input, is_training=is_training)

    return output

## =================================================================================================
## =================================================================================================

def unet_reg_v1_dropout(input, is_training, keep_prob, style, num_output_channels):

    model = seg_unet_reg_v1_dropout(unet_size=style, num_classes=num_output_channels, keep_prob = keep_prob, data_format='channels_last')
    output = model(inputs=input, is_training=is_training)

    return output

